#!/bin/bash

# ==============================================================================
# 脚本名称: check_afc7_hw.sh
# 功能描述: AFC-7 飞控底板硬件资源 (A35 侧) 全景巡检工具
# 检查目的: 确认 Linux (A35) 已完全释放硬实时外设，确立双核资源的明确权属。
# 运    行：用户在开发板上拿到这个脚本后，需要赋予它执行权限，然后才能运行。
#             (1)  chmod +x check_afc7_hw.sh
#             (2)  ./check_afc7_hw.sh
# ==============================================================================

echo "================================================================="
echo "    🚀 AFC-7 飞控底板硬件资源 (A35 侧) 全景巡检工具"
echo "================================================================="
echo "说明：本工具用于确认底层设备树 (.dtb) 是否正确配置。"
echo "请核对以下输出结果，以确保飞控相关资源已交由 M33 掌控。"
echo ""

echo -e "\e[1;34m[1. 串口 (UART/USART)]\e[0m"
echo "期望: 仅保留 ttySTM0 (A35调试终端)，原 usart1/3/4/5/6/8/9 应全部消失。"
ls -l /dev/ttySTM* 2>/dev/null
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[2. I2C 总线]\e[0m"
echo "期望: 无 i2c1 (40120000) 和 i2c3 (40140000)。"
cat /sys/class/i2c-adapter/i2c-*/name 2>/dev/null
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[3. SPI 总线]\e[0m"
echo "期望: 目录为空或无 spi1/4/7 主机节点。"
ls -l /sys/class/spi_master/ 2>/dev/null || echo -e "\e[1;32m(完美) 无 SPI 主机节点\e[0m"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[4. PWM 与定时器]\e[0m"
echo "期望: 目录为空，A35 未挂载任何飞控 PWM 驱动。"
ls -l /sys/class/pwm/ 2>/dev/null || echo -e "\e[1;32m(完美) 无 PWM 节点\e[0m"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[5. ADC 模数转换]\e[0m"
echo "期望: A35 未占用 ADC1/ADC2。"
ls -d /sys/bus/iio/devices/iio:device* 2>/dev/null || echo -e "\e[1;32m(完美) 无 ADC 节点\e[0m"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[6. CAN 总线]\e[0m"
echo "状态: 若分配给 M33 则无输出；若分配给 ROS2 则显示 can0。"
ip link show | grep can || echo -e "\e[1;32m(完美) 无 CAN 网络接口\e[0m"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[7. 视频输入 (CSI摄像 / 硬件解码)]\e[0m"
echo "期望: 存在 /dev/video 节点，供 A35 机载视觉计算机使用。"
ls -l /dev/video* 2>/dev/null || echo -e "\e[1;31m无视频节点\e[0m"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[8. 图形与显示 (GPU)]\e[0m"
echo "期望: 无物理显示屏输出，仅保留 card0 (纯硅片内部 3D GPU 加速)。"
ls -l /dev/dri/ 2>/dev/null || echo "无 DRI 图形节点"
echo "-----------------------------------------------------------------"

echo -e "\e[1;34m[9. USB 主机接口]\e[0m"
echo "期望: 存在 Linux Foundation root hub 及已挂载的外设。"
lsusb 2>/dev/null || echo "未安装 lsusb 命令"
echo "================================================================="
echo "                       🏁 巡检结束"
echo "================================================================="