#!/bin/bash

# ==============================================================================
# 脚本名称: AFC7LinuxBSPSetUp.sh
# 功能描述: AFC-7 智能飞控板 - 固件与双启动环境一键部署工具
# 运行环境: 宿主机 (WSL2 / Linux / macOS)
# ==============================================================================

# 定义目标开发板 IP 地址 (可按需修改)
TARGET_IP="192.168.0.10"
TARGET_USER="root"

echo -e "\e[1;36m=================================================================\e[0m"
echo -e "\e[1;36m      🚀 欢迎使用 AFC-7 飞控板 BSP 一键部署工具 v1.0\e[0m"
echo -e "\e[1;36m=================================================================\e[0m"
echo "即将向目标开发板 ($TARGET_IP) 推送更新..."
echo ""

# 1. 推送底层设备树固件与开机图形 Logo
echo -e "\e[1;33m[1/5] 正在推送设备树固件与启动 Logo 至 /boot/...\e[0m"
scp myb-afc7-2GB.dtb ${TARGET_USER}@${TARGET_IP}:/boot/
if [ $? -ne 0 ]; then echo -e "\e[1;31m推送失败，请检查网络连接或文件是否存在！\e[0m"; exit 1; fi

# 2. 推送引导配置文件 (确立双启动防变砖机制)
echo -e "\e[1;33m[2/5] 正在推送双启动引导配置...\e[0m"
scp myb-stm32mp257x-2GB_extlinux.conf ${TARGET_USER}@${TARGET_IP}:/boot/mmc1_extlinux/

# 3. 推送专属串口欢迎界面
echo -e "\e[1;33m[3/5] 正在推送 AFC-7 专属串口欢迎界面...\e[0m"
scp issue ${TARGET_USER}@${TARGET_IP}:/etc/issue

# 4. 推送硬件全景巡检脚本
echo -e "\e[1;33m[4/5] 正在推送硬件资源巡检脚本...\e[0m"
# 使用 ~/ 代表目标用户的真实家目录
scp check_afc7_hw.sh ${TARGET_USER}@${TARGET_IP}:~/

# 5. 远程执行权限赋予与重启
echo -e "\e[1;33m[5/5] 正在赋予执行权限并重启开发板...\e[0m"
# 技巧：使用 ssh 远程执行多条命令，而无需手动登录
# 同样使用 cd ~ 确保在真实的家目录下赋予权限
ssh ${TARGET_USER}@${TARGET_IP} "cd ~ && chmod +x check_afc7_hw.sh && sync && reboot"

echo -e "\e[1;32m=================================================================\e[0m"
echo -e "\e[1;32m   🎉 部署指令下发完毕！开发板正在重启，SSH 连接会断开属正常现象。\e[0m"
echo -e "\e[1;32m   等待重启完成后，请登录MobaXterm软件或串口终端                 \e[0m"
echo -e "\e[1;32m   运行 ./check_afc7_hw.sh 进行硬件资源权属确认。                \e[0m"
echo -e "\e[1;32m=================================================================\e[0m"